package com.kindArt.manager.json.Json001;

public class ActivityObj {
	private String title;// 标题
	private String abstractInfo;// 简介
	private String bigImgURL;// 大图地址
	private String likedNum;// 点赞数
	private String collectionNum;// 收藏数
	private String commentNum;// 评论数
	private String activityId;// 社区动态ID
	private String publisherId;// 发布人ID
	private String publisherName;// 发布人昵称
	private String publisherHPURL;// 发布人头像地址
	private String lastCommentTime;// 最新评论时间

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAbstractInfo() {
		return abstractInfo;
	}

	public void setAbstractInfo(String abstractInfo) {
		this.abstractInfo = abstractInfo;
	}

	public String getBigImgURL() {
		return bigImgURL;
	}

	public void setBigImgURL(String bigImgURL) {
		this.bigImgURL = bigImgURL;
	}

	public String getLikedNum() {
		return likedNum;
	}

	public void setLikedNum(String likedNum) {
		this.likedNum = likedNum;
	}

	public String getCollectionNum() {
		return collectionNum;
	}

	public void setCollectionNum(String collectionNum) {
		this.collectionNum = collectionNum;
	}

	public String getCommentNum() {
		return commentNum;
	}

	public void setCommentNum(String commentNum) {
		this.commentNum = commentNum;
	}

	public String getActivityId() {
		return activityId;
	}

	public void setActivityId(String activityId) {
		this.activityId = activityId;
	}

	public String getPublisherId() {
		return publisherId;
	}

	public void setPublisherId(String publisherId) {
		this.publisherId = publisherId;
	}

	public String getPublisherName() {
		return publisherName;
	}

	public void setPublisherName(String publisherName) {
		this.publisherName = publisherName;
	}

	public String getPublisherHPURL() {
		return publisherHPURL;
	}

	public void setPublisherHPURL(String publisherHPURL) {
		this.publisherHPURL = publisherHPURL;
	}

	public String getLastCommentTime() {
		return lastCommentTime;
	}

	public void setLastCommentTime(String lastCommentTime) {
		this.lastCommentTime = lastCommentTime;
	}

}
