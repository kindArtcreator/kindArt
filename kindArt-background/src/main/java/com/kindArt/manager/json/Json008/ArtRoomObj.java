package com.kindArt.manager.json.Json008;

public class ArtRoomObj {

	private String artRoomId;// 画室id
	private String artRoomName;// 画室名称
	private String background;// 背景图
	private String bussHPURL;// 企业头像地址
	private String abstractInfo;// 简介

	public String getArtRoomId() {
		return artRoomId;
	}

	public void setArtRoomId(String artRoomId) {
		this.artRoomId = artRoomId;
	}

	public String getArtRoomName() {
		return artRoomName;
	}

	public void setArtRoomName(String artRoomName) {
		this.artRoomName = artRoomName;
	}

	public String getBackground() {
		return background;
	}

	public void setBackground(String background) {
		this.background = background;
	}

	public String getBussHPURL() {
		return bussHPURL;
	}

	public void setBussHPURL(String bussHPURL) {
		this.bussHPURL = bussHPURL;
	}

	public String getAbstractInfo() {
		return abstractInfo;
	}

	public void setAbstractInfo(String abstractInfo) {
		this.abstractInfo = abstractInfo;
	}

}
