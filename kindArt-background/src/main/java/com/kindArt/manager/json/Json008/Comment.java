package com.kindArt.manager.json.Json008;

public class Comment {

	private String commentPersonId;// 评论人ID
	private String commentPersonName;// 评论人名称
	private String commentContent;// 评论内容
	private String commentTime;// 评论时间
	private String commentPersonHPURL;// 评论人头像ID
	private String answerID;// 最新回复人ID
	private String answerName;// 最新回复人名称
	private String answerHPURL;// 最新回复人头像ID
	private String answerContent;// 最新回复内容
	private String seq;// 序号

	public String getCommentPersonId() {
		return commentPersonId;
	}

	public void setCommentPersonId(String commentPersonId) {
		this.commentPersonId = commentPersonId;
	}

	public String getCommentPersonName() {
		return commentPersonName;
	}

	public void setCommentPersonName(String commentPersonName) {
		this.commentPersonName = commentPersonName;
	}

	public String getCommentContent() {
		return commentContent;
	}

	public void setCommentContent(String commentContent) {
		this.commentContent = commentContent;
	}

	public String getCommentTime() {
		return commentTime;
	}

	public void setCommentTime(String commentTime) {
		this.commentTime = commentTime;
	}

	public String getCommentPersonHPURL() {
		return commentPersonHPURL;
	}

	public void setCommentPersonHPURL(String commentPersonHPURL) {
		this.commentPersonHPURL = commentPersonHPURL;
	}

	public String getAnswerID() {
		return answerID;
	}

	public void setAnswerID(String answerID) {
		this.answerID = answerID;
	}

	public String getAnswerName() {
		return answerName;
	}

	public void setAnswerName(String answerName) {
		this.answerName = answerName;
	}

	public String getAnswerHPURL() {
		return answerHPURL;
	}

	public void setAnswerHPURL(String answerHPURL) {
		this.answerHPURL = answerHPURL;
	}

	public String getAnswerContent() {
		return answerContent;
	}

	public void setAnswerContent(String answerContent) {
		this.answerContent = answerContent;
	}

	public String getSeq() {
		return seq;
	}

	public void setSeq(String seq) {
		this.seq = seq;
	}

}
