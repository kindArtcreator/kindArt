package com.kindArt.manager.json.Json016;

public class MyConfig {

	private String personHPURL;// 个人头像地址
	private String weChatNo;// 微信号
	private String sex;// 性别
	private String birthday;// 出生日期
	private String constellation;// 星座
	private String address;// 地址
	private String autograph;// 个人签名

	public String getPersonHPURL() {
		return personHPURL;
	}

	public void setPersonHPURL(String personHPURL) {
		this.personHPURL = personHPURL;
	}

	public String getWeChatNo() {
		return weChatNo;
	}

	public void setWeChatNo(String weChatNo) {
		this.weChatNo = weChatNo;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getBirthday() {
		return birthday;
	}

	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}

	public String getConstellation() {
		return constellation;
	}

	public void setConstellation(String constellation) {
		this.constellation = constellation;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getAutograph() {
		return autograph;
	}

	public void setAutograph(String autograph) {
		this.autograph = autograph;
	}
}
