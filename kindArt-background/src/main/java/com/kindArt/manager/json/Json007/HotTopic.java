package com.kindArt.manager.json.Json007;

public class HotTopic {
	private String topicId;// 话题ID
	private String topicURL;// 话题图片地址
	private String topicCommentNum;// 话题评论人数
	private String topic;// 话题标题

	public String getTopicId() {
		return topicId;
	}

	public void setTopicId(String topicId) {
		this.topicId = topicId;
	}

	public String getTopicURL() {
		return topicURL;
	}

	public void setTopicURL(String topicURL) {
		this.topicURL = topicURL;
	}

	public String getTopicCommentNum() {
		return topicCommentNum;
	}

	public void setTopicCommentNum(String topicCommentNum) {
		this.topicCommentNum = topicCommentNum;
	}

	public String getTopic() {
		return topic;
	}

	public void setTopic(String topic) {
		this.topic = topic;
	}

}
