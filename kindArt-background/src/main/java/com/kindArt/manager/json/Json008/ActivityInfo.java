package com.kindArt.manager.json.Json008;

public class ActivityInfo {
	private String imgURL;// 大图地址
	private String activityTitle;// 活动标题
	private String activityPublishTime;// 活动发布日期
	private String activityContent;// 活动内容
	private String activityAddress;// 活动地址
	private String activityTime;// 活动时间
	private String personNum;// 人数
	private String chargeType;// 收费方式
	private String fee;// 费用
	private String consult;// 咨询与报名

	public String getImgURL() {
		return imgURL;
	}

	public void setImgURL(String imgURL) {
		this.imgURL = imgURL;
	}

	public String getActivityTitle() {
		return activityTitle;
	}

	public void setActivityTitle(String activityTitle) {
		this.activityTitle = activityTitle;
	}

	public String getActivityPublishTime() {
		return activityPublishTime;
	}

	public void setActivityPublishTime(String activityPublishTime) {
		this.activityPublishTime = activityPublishTime;
	}

	public String getActivityContent() {
		return activityContent;
	}

	public void setActivityContent(String activityContent) {
		this.activityContent = activityContent;
	}

	public String getActivityAddress() {
		return activityAddress;
	}

	public void setActivityAddress(String activityAddress) {
		this.activityAddress = activityAddress;
	}

	public String getActivityTime() {
		return activityTime;
	}

	public void setActivityTime(String activityTime) {
		this.activityTime = activityTime;
	}

	public String getPersonNum() {
		return personNum;
	}

	public void setPersonNum(String personNum) {
		this.personNum = personNum;
	}

	public String getChargeType() {
		return chargeType;
	}

	public void setChargeType(String chargeType) {
		this.chargeType = chargeType;
	}

	public String getFee() {
		return fee;
	}

	public void setFee(String fee) {
		this.fee = fee;
	}

	public String getConsult() {
		return consult;
	}

	public void setConsult(String consult) {
		this.consult = consult;
	}

}
