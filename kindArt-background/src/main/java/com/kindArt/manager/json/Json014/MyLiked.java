package com.kindArt.manager.json.Json014;

public class MyLiked {

	private String personId;// 相关人ID
	private String personName;// 相关人昵称
	private String fansNum;// 粉丝数
	private String personHPURL;// 相关人人头像地址
	private String personAutograph;// 相关人签名

	public String getPersonId() {
		return personId;
	}

	public void setPersonId(String personId) {
		this.personId = personId;
	}

	public String getPersonName() {
		return personName;
	}

	public void setPersonName(String personName) {
		this.personName = personName;
	}

	public String getFansNum() {
		return fansNum;
	}

	public void setFansNum(String fansNum) {
		this.fansNum = fansNum;
	}

	public String getPersonHPURL() {
		return personHPURL;
	}

	public void setPersonHPURL(String personHPURL) {
		this.personHPURL = personHPURL;
	}

	public String getPersonAutograph() {
		return personAutograph;
	}

	public void setPersonAutograph(String personAutograph) {
		this.personAutograph = personAutograph;
	}

}
